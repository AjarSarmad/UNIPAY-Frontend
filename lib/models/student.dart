class Student {
  String nu_id = "";
  String firstName = "";
  String lastName = "";
  String contact = "";
  String email = "";
  String password = "";
  String guardianFullName = "";
  String guardianContact = "";
  String address = "";
  String accountNo = "";
  String balance = "0";

  Student() {}
  String getNu_id() {
    return nu_id;
  }

  void setNu_id(String nu_id) {
    this.nu_id = nu_id;
  }

  String getFirstName() {
    return firstName;
  }

  void setFirstName(String firstName) {
    this.firstName = firstName;
  }

  String getLastName() {
    return lastName;
  }

  void setLastName(String lastName) {
    this.lastName = lastName;
  }

  String getContact() {
    return contact;
  }

  void setContact(String contact) {
    this.contact = contact;
  }

  String getEmail() {
    return email;
  }

  void setEmail(String email) {
    this.email = email;
  }

  String getPassword() {
    return password;
  }

  void setPassword(String password) {
    this.password = password;
  }

  String getGuardianFullName() {
    return guardianFullName;
  }

  void setGuardianFullName(String guardianFullName) {
    this.guardianFullName = guardianFullName;
  }

  String getGuardianContact() {
    return guardianContact;
  }

  void setGuardianContact(String guardianContact) {
    this.guardianContact = guardianContact;
  }

  String getAddress() {
    return address;
  }

  void setAddress(String address) {
    this.address = address;
  }

  String getAccountNo() {
    return accountNo;
  }

  void setAccountNo(String accountNo) {
    this.accountNo = accountNo;
  }

  String getBalance() {
    return balance;
  }

  void setBalance(String balance) {
    this.balance = balance;
  }
}
